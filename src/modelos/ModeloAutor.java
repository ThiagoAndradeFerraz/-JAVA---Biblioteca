package modelos;

public class ModeloAutor {

	private int idAutor;
	private String nomeAutor;
	private String pesquisaAutor;
	
	
	public int getIdAutor() {
		return idAutor;
	}
	public void setIdAutor(int idAutor) {
		this.idAutor = idAutor;
	}
	
	
	public String getNomeAutor() {
		return nomeAutor;
	}
	public void setNomeAutor(String nomeAutor) {
		this.nomeAutor = nomeAutor;
	}
	
	
	public String getPesquisaAutor() {
		return pesquisaAutor;
	}
	public void setPesquisaAutor(String pesquisaAutor) {
		this.pesquisaAutor = pesquisaAutor;
	}
}
